
function toggle(){
    let icon = document.getElementById("whatsapp-logo")
    if (icon.style.display === "none"){
        icon.style.display = "inline"
    }else{
        icon.style.display = "none"
    }
    if (icon.style.visibility === "hidden"){
    }else{
        (icon.style.visibility = "visible")
    }
}
